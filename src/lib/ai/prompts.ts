/**
 * 构建带用户笔记上下文的系统消息（原有版本，供内部向后兼容）
 */
export function buildSystemPromptWithContext(
  modePrompt: string,
  context: string
): string {
  return buildSystemPromptWithPersonaAndContext(modePrompt, context, '', '');
}

/**
 * 构建完整系统提示词（三层上下文版本）
 *
 * @param modePrompt       - 当前模式提示词（统一/回溯/行动）
 * @param mainContext      - 主检索召回的 Memo 上下文（相关性优先）
 * @param personaAnchor    - 用户个性锚点（来自 memories active 条目）
 * @param recentBaseline   - 最近笔记基线（最近 14 天摘要，提供「当下感」）
 * @param principlesTension - 准则张力提示（镜子模式：指出行为与准则的张力）
 */
export function buildSystemPromptWithPersonaAndContext(
  modePrompt: string,
  mainContext: string,
  personaAnchor: string,
  recentBaseline: string,
  principlesTension?: string,
): string {
  const sections: string[] = [];

  sections.push(`${SYSTEM_PROMPT}\n\n${modePrompt}`);

  // 个性锚点（始终存在，了解这个人是谁）
  if (personaAnchor.trim()) {
    sections.push(`---

以下是从用户长期记录中提炼的个性锚点。这不是用户的标签或诊断，而是他自己的记录所呈现出的稳定轮廓——你可以用它来理解「这个人大概是谁」，尤其在笔记召回不足时，帮助你给出有共鸣而非泛泛的回应：

<persona_anchor>
${personaAnchor}
</persona_anchor>`);
  }

  // 准则张力提示（镜子模式）
  if (principlesTension && principlesTension.trim()) {
    sections.push(`---

以下是用户过去主动保存的行为与思维准则。请把它们当作「用户与自己的约定」，而不是评判标准。
当你在笔记中发现用户的行为或想法与这些准则之间存在明显张力时，可以温和地指出这个张力，让用户自己去看见——而不是用准则来评价或纠正用户：

<user_principles>
${principlesTension}
</user_principles>`);
  }

  // 主检索 Memo 上下文
  sections.push(`---

以下是与用户问题相关的笔记记录，请在回答中优先参考这些内容：

引用协议（非常重要，必须严格遵守）：
- 当一句话使用某条笔记作为证据时，在该句话后紧跟 [[memo:完整笔记ID]]，例如：[[memo:6dfb589a-1234-4abc-9def-0123456789ab]]。
- 使用长期记忆或个性锚点时，紧跟 [[memory:记忆ID]]；使用用户准则时，紧跟 [[principle:准则ID]]。
- 必须包含来源类型前缀。严禁输出 [[persona_anchor]]、[[6dfb589a]] 等裸标记，也不要把 XML 区块名当成引用。
- 只能使用下方上下文明确提供的对应类型 ID（笔记、长期记忆、准则）。必须逐字复制完整 ID，不得编造、缩写、改写或混用类型。
- 一个结论有多条证据时可以连续放多个标记。
- 推断可以引用其依据，但必须用「可能、看起来、这提示」等语言标明推断边界。
- 通用建议不添加引用标记；不得只引用 AI 检索索引来支撑事实。
- 逐字引用日记或笔记原话时，必须使用 Markdown 引用块，每行以 "> " 开头；引用块后另起一行附对应的 [[memo:笔记ID]]。
- 不要把引用标记放进代码块，不要解释标记语法。

<memo_context>
${mainContext || '（没有找到与问题直接相关的笔记记录）'}
</memo_context>`);

  // 最近笔记基线（当下感锚点）
  if (recentBaseline.trim()) {
    sections.push(`---

以下是用户最近 14 天内的笔记摘要，提供当下状态的背景参考。它们未必与问题直接相关，但能帮助你感知用户此刻所在的位置：

<recent_baseline>
${recentBaseline}
</recent_baseline>`);
  }

  sections.push(`---

请基于以上记录回答用户的问题。上下文是高召回候选集，其中可能包含弱相关内容；请自行筛选、交叉验证，不必使用每一条。没有足够证据时坦诚说明，不要为了给出确定答案而扩大推断。`);

  return sections.join('\n\n');
}

/**
 * 根据模式获取对应的提示词
 */
export function getModePrompt(mode: 'unified' | 'retrospect' | 'action'): string {
  if (mode === 'retrospect') return RETROSPECT_PROMPT;
  if (mode === 'action') return ACTION_PROMPT;
  return UNIFIED_CONVERSATION_PROMPT;
}
